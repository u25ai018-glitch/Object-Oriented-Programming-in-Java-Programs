 class SquareandCubes {
    public static void main (String args[]){
        System.out.println("Square of 0 to 10 in table format is:");
        for(int i=0;i<=10;i++){
            System.out.printf("%d*%d=%d\n",i,i,(i*i));
        }
        System.out.println("\n");
         System.out.println("Cube of 0 to 10 in table format is:");
        for(int i=0;i<=10;i++){
            System.out.printf("%d*%d*%d=%d\n",i,i,i,(i*i*i));
        }


    }
    
}
